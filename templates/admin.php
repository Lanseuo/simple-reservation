<div class="wrap">
    <h1>SimpleReservation</h1>

    <p>Mit diesem Plugin können Raumreservierungen ganz einfach vorgenommen und verwaltet werden.</p>

    <h2>Anleitung</h2>

    <h4>Räume hinzufügen</h4>
    <p>Um dieses Plugin verwenden zu können, müssen Sie als erstes die Räume hinzufügen, die mit dem Plugin verwaltet werden sollen. Dies können Sie im <a href="<?php menu_page_url( 'simple_reservation_rooms' ); ?>">Raumverwaltungsbereich</a> von diesem Plugin tun.</p>

    <h4>Reservierungsansicht hinzufügen</h4>
    <p>Damit Nutzer einen Raum reservieren können, müssen Sie die Reservierungsansicht auf einer Seite hinzufügen. Damit die Reservierungsansicht auf einer Seite sichtbar wird, fügen Sie den folgenden Shortcode auf der gewünschten Seite in den Editor ein:</p>
    <code>[simplereservation]</code>
    <p>Nun finden Sie auf dieser Seite einen Abschnitt über den die Nutzer die verschiedenen Räume verwalten können.</p>
</div>